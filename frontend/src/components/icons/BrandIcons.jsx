// Component to render brand icon with proper styling
export const BrandIcon = ({ platform, brand, size = 24, className = "" }) => {
  const iconKey = platform || brand;
  const iconData = BrandIcons[iconKey];

  if (!iconData) {
    return (
      <div
        className={`flex items-center justify-center bg-gray-200 rounded ${className}`}
        style={{ width: size, height: size }}
      >
        <span style={{ fontSize: size * 0.6 }}>?</span>
      </div>
    );
  }

  const IconComponent = iconData.icon;

  // Convert hex to rgba for background
  const hexToRgba = (hex, alpha) => {
    const r = parseInt(hex.slice(1, 3), 16);
    const g = parseInt(hex.slice(3, 5), 16);
    const b = parseInt(hex.slice(5, 7), 16);
    return `rgba(${r}, ${g}, ${b}, ${alpha})`;
  };

  return (
    <div
      className={`flex items-center justify-center rounded-lg ${className}`}
      style={{
        width: size,
        height: size,
        backgroundColor: hexToRgba(iconData.color, 0.15),
        border: `1px solid ${hexToRgba(iconData.color, 0.3)}`,
      }}
    >
      <IconComponent
        width={size * 0.7}
        height={size * 0.7}
        style={{
          color: iconData.color,
          fill: iconData.color
        }}
        fill={iconData.color}
        color={iconData.color}
      />
    </div>
  );
};


